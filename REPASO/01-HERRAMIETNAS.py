## Pycharm
'''
Pycharm es un ambiente para el desarrollo de código y será la principal herramienta que estaremos usando a lo largo
del semeste, este programa cuenta con distintas herramientas que facilitan el desarrollo e implementación de los 
códigos. 
en primer lugar se tiene la opción de instalar distintos plugins que facilitan la visualización de la información
entre estos se encuentran 
    - rainbow brackets: cambial el color de los parentesis de manera que se facilita leer la información al interior
    de estos 
    - rainbow indent: cambia el color de la indentación, esto permite verificar con gran facilidad en que sección 
    se encuentra cada código 
por otra parte cuenta con opciones que facilitan la depuración del código (elementos que estaremos abordando a 
lo largo del curso) entre estas se encuentan
    - la consola: permite correr con gran facilidad lineas individuales de código
    - herramientas especificas para la depuración 
    - celdas (plug-in): una herramienta que permite dividir el código en distintas secciones y ejecutarlas de 
    manera individula. 
para las instalación de plug-ins 
File> Settings> plugins> "Buscar lo que se desea instalar"

En cuanto al uso de este programa, es necesario crear un proyecto, lo que se puede hacer siguiendo las indicaciones
que se encuentran en la pantalla inicial del programa, en caso de que se cree un proyecto sin un interprete 
(el lenguaje de programación que se desea utilizar para ejecutar el código) se puede agregar haciendo click en 
"add configuration" en la parte superior derecha del programa y siguiendo los pasos que se indican en el programa
'''
