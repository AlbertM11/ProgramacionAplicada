## StackOverflow
'''
StackOverflow es un sitio donde es posble publicar preguntas relacionadas con informatica y recibir una respuesta, 
el objetivo de este sitio es encotrar distintas soluciones a problemas con los que se pueden llegar a encontrar
lo más probable es que alguien sepa hacer lo que usteden estan intentando hacer o bien los pueda guiar en el 
proceso. 
Recuerden que esto es solo una herramienta, pueden ver un codigo que funciona pero es necesario que lo entiendan 
antes de implementarlo, de lo contrario no estan aprendiendo y no progresarán. 
metodología para usar esta herramietna 
1. buscar la pregunta o error que les aparece 
2. leer las primeras soluciones 
3. implementar de manera textual la solución propuesta 
4. modificar según las necesidades de su programa.
'''
